func TestUpsertAutogeneratedDoc_Creates(t *testing.T) {
	t.Parallel()

	store := &mockPMDocStore{
		getByOrgST: map[string]models.PMDocument{}, // empty — no existing doc
	}
	svc := &Service{
		pmDocuments: store,
		logger:      zerolog.Nop(),
	}

	err := svc.upsertAutogeneratedDoc(context.Background(), uuid.New(), "# New Context")
	require.NoError(t, err)
	require.Len(t, store.created, 1, "should create one doc")
	require.Equal(t, "# New Context", store.created[0].Content)
	require.Equal(t, models.PMDocSourceAutogenerated, store.created[0].SourceType)
	require.Equal(t, -1, store.created[0].SortOrder)
	require.NotNil(t, store.created[0].LastSyncedAt)
}

func TestUpsertAutogeneratedDoc_Updates(t *testing.T) {
	t.Parallel()

	existingID := uuid.New()
	store := &mockPMDocStore{
		getByOrgST: map[string]models.PMDocument{
			models.PMDocSourceAutogenerated: {
				ID:      existingID,
				Content: "old content",
			},
		},
	}
	svc := &Service{
		pmDocuments: store,
		logger:      zerolog.Nop(),
	}

	err := svc.upsertAutogeneratedDoc(context.Background(), uuid.New(), "# Updated Content")
	require.NoError(t, err)
	require.Empty(t, store.created, "should not create")
	require.Len(t, store.updated, 1, "should update existing doc")
	require.Equal(t, "# Updated Content", store.updated[0].Content)
	require.NotNil(t, store.updated[0].LastSyncedAt)
}

func TestBuildBootstrapSeed_WithProductContext(t *testing.T) {
	t.Parallel()

	orgID := uuid.New()
	settings := models.OrgSettings{
		ProductContext: &models.ProductContext{
			Direction: "AI-first",
		},
	}
	settingsJSON, _ := json.Marshal(settings)

	svc := &Service{
		orgs: &mockOrgStore{
			org: models.Organization{
				ID:       orgID,
				Settings: settingsJSON,
			},
		},
		pmDocuments: &mockPMDocStore{
			docs: []models.PMDocument{
				{Title: "Roadmap", DocType: "roadmap", SourceType: models.PMDocSourceNotion},
			},
		},
		logger: zerolog.Nop(),
	}

	repo := &models.Repository{FullName: "org/repo"}
	creds := &integrationCredentials{github: true, sentry: nil}

	seedJSON, err := svc.buildBootstrapSeed(context.Background(), orgID, repo, creds)
	require.NoError(t, err)

	var seed map[string]any
	require.NoError(t, json.Unmarshal(seedJSON, &seed))
	require.Contains(t, seed, "product_context")
	require.Contains(t, seed, "connected_integrations")
	require.Contains(t, seed, "existing_pm_documents")

	integrations := seed["connected_integrations"].([]any)
	require.Contains(t, integrations, "github")
}

