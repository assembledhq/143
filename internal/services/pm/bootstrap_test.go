// --- mock pmDocumentStore ---

type mockPMDocStore struct {
	docs       []models.PMDocument
	created    []*models.PMDocument
	updated    []*models.PMDocument
	deleted    []uuid.UUID
	getByOrgST map[string]models.PMDocument // keyed by sourceType
	getByID    map[uuid.UUID]models.PMDocument
	createErr  error
	updateErr  error
}

func (m *mockPMDocStore) ListByOrg(_ context.Context, _ uuid.UUID) ([]models.PMDocument, error) {
	return m.docs, nil
}

func (m *mockPMDocStore) ListByOrgExcludeSourceType(_ context.Context, _ uuid.UUID, exclude string, limit int) ([]models.PMDocument, error) {
	var result []models.PMDocument
	for _, d := range m.docs {
		if d.SourceType != exclude {
			result = append(result, d)
		}
		if len(result) >= limit {
			break
		}
	}
	return result, nil
}

func (m *mockPMDocStore) GetByOrgAndSourceType(_ context.Context, _ uuid.UUID, sourceType string) (models.PMDocument, error) {
	if doc, ok := m.getByOrgST[sourceType]; ok {
		return doc, nil
	}
	return models.PMDocument{}, pgx.ErrNoRows
}

func (m *mockPMDocStore) ListByOrgAndSourceType(_ context.Context, _ uuid.UUID, sourceType string) ([]models.PMDocument, error) {
	var result []models.PMDocument
	for _, d := range m.docs {
		if d.SourceType == sourceType {
			result = append(result, d)
		}
	}
	return result, nil
}

func (m *mockPMDocStore) Create(_ context.Context, doc *models.PMDocument) error {
	if doc.ID == uuid.Nil {
		doc.ID = uuid.New()
	}
	m.created = append(m.created, doc)
	return m.createErr
}

func (m *mockPMDocStore) Update(_ context.Context, doc *models.PMDocument) error {
	m.updated = append(m.updated, doc)
	return m.updateErr
}

func (m *mockPMDocStore) Delete(_ context.Context, _ uuid.UUID, docID uuid.UUID) error {
	m.deleted = append(m.deleted, docID)
	return nil
}

func (m *mockPMDocStore) DeleteByOrgAndSourceType(_ context.Context, _ uuid.UUID, sourceType string) error {
	return nil
}

func (m *mockPMDocStore) GetByID(_ context.Context, _ uuid.UUID, docID uuid.UUID) (models.PMDocument, error) {
	if doc, ok := m.getByID[docID]; ok {
		return doc, nil
	}
	return models.PMDocument{}, pgx.ErrNoRows
}

func (m *mockPMDocStore) Begin(_ context.Context) (pgx.Tx, error) {
	return mockTx{}, nil
}

func (m *mockPMDocStore) WithTx(_ pgx.Tx) *db.PMDocumentStore {
	// Return a PMDocumentStore wrapping a no-op DBTX. In unit tests the
	// returned store isn't exercised — callers interact with mockPMDocStore
	// directly. If a test needs transactional behaviour, use pgxmock instead.
	return db.NewPMDocumentStore(nil)
}

// mockTx is a minimal pgx.Tx implementation for unit tests.
type mockTx struct{ pgx.Tx }

func (mockTx) Commit(_ context.Context) error   { return nil }
func (mockTx) Rollback(_ context.Context) error { return nil }

// --- mock credential store ---

type mockCredStore struct {
	creds map[models.ProviderName]*models.DecryptedCredential
}

// withDefaultStatus applies Status="active" when the test fixture didn't set
// one. The legacy fallback resolver in agent.AgentEnv now filters by status,
// and these PM tests pre-date that filter — every fixture would otherwise
// need to repeat `Status: "active"` for behavior that's already production
// reality.
func (m *mockCredStore) withDefaultStatus(cred *models.DecryptedCredential) *models.DecryptedCredential {
	if cred == nil || cred.Status != "" {
		return cred
	}
	copy := *cred
	copy.Status = models.CredentialStatusActive
	return &copy
}

func (m *mockCredStore) Get(_ context.Context, _ uuid.UUID, provider models.ProviderName) (*models.DecryptedCredential, error) {
	if c, ok := m.creds[provider]; ok {
		return m.withDefaultStatus(c), nil
	}
	return nil, pgx.ErrNoRows
}

func (m *mockCredStore) ListByProvider(_ context.Context, _ uuid.UUID, provider models.ProviderName) ([]models.DecryptedCredential, error) {
	if c, ok := m.creds[provider]; ok {
		return []models.DecryptedCredential{*m.withDefaultStatus(c)}, nil
	}
	return nil, pgx.ErrNoRows
}

